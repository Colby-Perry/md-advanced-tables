/**
 * Represents cell alignment.
 *
 * - `Alignment.DEFAULT` - Use default alignment.
 * - `Alignment.LEFT` - Align left.
 * - `Alignment.RIGHT` - Align right.
 * - `Alignment.CENTER` - Align center.
 *
 * @type {Object}
 */
export const Alignment = Object.freeze({
  DEFAULT: "default",
  LEFT   : "left",
  RIGHT  : "right",
  CENTER : "center"
});
